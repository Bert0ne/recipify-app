export const uploadRecipe = async function (newRecipe, recipeIng) {
  try {
    const ingredients = [];

    recipeIng.map(rec => {
      const qua = rec
        .querySelector('input[name="amount"]')
        .value.replace(',', '.');

      const uni = rec.querySelector('select').value;
      const desc = rec.querySelector('input[name="ingredient"]').value;

      if (isNaN(qua))
        throw new Error(`Wrong amount format! Please use number only!`);
      if (qua <= 0)
        throw new Error(
          `Wrong amount format! Please use number bigger than 0!`
        );

      ingredients.push({
        quantity: qua ? +qua : null,
        unit: uni ? uni : '',
        description: desc,
      });
    });

    const recipe = {
      title: newRecipe.title,
      source_url: newRecipe.sourceUrl,
      image_url: newRecipe.image,
      publisher: newRecipe.publisher,
      cooking_time: +newRecipe.cookingTime,
      servings: +newRecipe.servings,
      ingredients,
    };
    const data = await AJAX(`${API_URL}?key=${KEY}`, recipe);
    state.recipe = createRecipeObject(data);
    addBookmark(state.recipe);
  } catch (err) {
    throw err;
  }
};
